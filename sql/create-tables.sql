create table member (
	id INT,
	first_name VARCHAR(50),
	last_name VARCHAR(50),
	email VARCHAR(50),
	gender VARCHAR(50),
	tele_phone VARCHAR(50),
	userid VARCHAR(50)
);

create table product (
	product_id INT,
	product_name VARCHAR(50),
	description VARCHAR(50),
	product_image VARCHAR(50),
	product_brand VARCHAR(50),
	product_style VARCHAR(50)
);